VOICE = "zh-CN-YunjianNeural"
OUTPUT_DIR = "h:\\系统科学\\价值链物理学\\audio_sonata"

async def generate_voiceover(text, filename):
    filepath = os.path.join(OUTPUT_DIR, filename)
    print(f"Generating voiceover: {filename}...")
    communicate = edge_tts.Communicate(text, VOICE, rate="+0%")
    await communicate.save(filepath)
    print(f"Finished: {filename}")

async def main():
    if not os.path.exists(OUTPUT_DIR):
        os.makedirs(OUTPUT_DIR)
        
    tasks = []
    # Process sequentially or concurrently. To avoid rate limits, we run sequentially with slight delay.
    for text, filename in AUDIO_SEGMENTS:
        await generate_voiceover(text, filename)
        await asyncio.sleep(1.0)
    print("All audio files generated successfully!")

if __name__ == "__main__":
    asyncio.run(main())
